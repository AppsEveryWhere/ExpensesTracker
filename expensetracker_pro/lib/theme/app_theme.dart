      ),
    );
  }

  /// Monospace text style for financial data display
  static TextStyle getMonospaceStyle({
    required bool isLight,
    double fontSize = 16,
    FontWeight fontWeight = FontWeight.w400,
  }) {
    return GoogleFonts.robotoMono(
      fontSize: fontSize,
      fontWeight: fontWeight,
      color: isLight ? textPrimaryLight : textPrimaryDark,
      letterSpacing: 0.5,
    );
  }

  /// Helper method to get status colors
  static Color getStatusColor(String status) {
    switch (status.toLowerCase()) {
      case 'success':
      case 'completed':
      case 'paid':
        return successColor;
      case 'warning':
      case 'pending':
      case 'due':
        return warningColor;
      case 'error':
      case 'failed':
      case 'overdue':
        return errorColor;
      default:
        return textSecondaryLight;
    }
  }
